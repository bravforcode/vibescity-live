#!/usr/bin/env node
/**
 * Mobile Device Matrix Generator
 * bun run test:device-matrix
 * bun run test:device-matrix --auto   (writes playwright.device-matrix.config.ts)
 *
 * Reads device-matrix-th.json and generates:
 * - Playwright project configurations per tier (A/B/C) × orientation (portrait/landscape)
 * - Network lane summary (6 Thai carriers with RTT/bandwidth/loss)
 * - Total test combination count
 *
 * Flags:
 *   --auto   Write generated config to playwright.device-matrix.config.ts
 */

import fs from "node:fs/promises";
import path from "node:path";

const ROOT = process.cwd();
const MATRIX_PATH = path.join(ROOT, "device-matrix-th.json");
const REPORT_PATH = path.join(ROOT, "reports/ci/device-matrix.json");
const CONFIG_OUTPUT = path.join(ROOT, "playwright.device-matrix.config.ts");

const IS_AUTO = process.argv.includes("--auto");

// Viewport sizes per tier (representative real-device dimensions)
const TIER_VIEWPORTS = {
	A: { width: 393, height: 852 }, // iPhone 15 Pro
	B: { width: 412, height: 915 }, // Samsung Galaxy A55
	C: { width: 360, height: 780 }, // Budget Android
};

// User-agent strings per tier
const TIER_USER_AGENTS = {
	A: "Mozilla/5.0 (iPhone; CPU iPhone OS 17_0 like Mac OS X) AppleWebKit/605.1.15 (KHTML, like Gecko) Version/17.0 Mobile/15E148 Safari/604.1",
	B: "Mozilla/5.0 (Linux; Android 14; SM-A556E) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/124.0.0.0 Mobile Safari/537.36",
	C: "Mozilla/5.0 (Linux; Android 12; Redmi 10A) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/110.0.0.0 Mobile Safari/537.36",
};

const main = async () => {
	const raw = await fs.readFile(MATRIX_PATH, "utf8").catch(() => null);
	if (!raw) {
		console.error(`ERROR: ${MATRIX_PATH} not found`);
		process.exit(1);
	}

	const matrix = JSON.parse(raw);
	const tiers = Object.keys(matrix.tiers);
	const carriers = matrix.carriers;
	const projects = [];

	for (const tier of tiers) {
		const tierInfo = matrix.tiers[tier];
		const viewport = TIER_VIEWPORTS[tier];
		const userAgent = TIER_USER_AGENTS[tier];

		if (!viewport || !userAgent) continue;

		// Portrait
		projects.push({
			name: `Tier-${tier}-Portrait`,
			tier,
			description: tierInfo.description,
			target_fps: tierInfo.target_fps,
			orientation: "portrait",
			viewport,
			userAgent,
			examples: tierInfo.examples,
		});

		// Landscape
		projects.push({
			name: `Tier-${tier}-Landscape`,
			tier,
			description: tierInfo.description,
			target_fps: tierInfo.target_fps,
			orientation: "landscape",
			viewport: { width: viewport.height, height: viewport.width },
			userAgent,
			examples: tierInfo.examples,
		});
	}

	const networkLanes = carriers.map((carrier) => ({
		carrier,
		...matrix.network_lanes[carrier],
	}));

	const matrixOutput = {
		timestamp: new Date().toISOString(),
		country: matrix.country,
		projects,
		networkLanes,
		testCombinations: projects.length * networkLanes.length,
		protocol: matrix.baseline_protocol,
	};

	// Console summary
	console.log(
		`Device Matrix: ${projects.length} projects × ${networkLanes.length} network lanes = ${matrixOutput.testCombinations} combinations\n`,
	);
	console.log("Projects:");
	for (const proj of projects) {
		console.log(
			`  ${proj.name.padEnd(24)} ${proj.viewport.width}x${proj.viewport.height} | target ${proj.target_fps}fps | ${proj.description}`,
		);
	}
	console.log("\nNetwork lanes:");
	for (const lane of networkLanes) {
		console.log(
			`  ${lane.carrier.padEnd(20)} ${lane.rtt_ms}ms RTT | ${lane.bandwidth_mbps}Mbps | ${lane.packet_loss_percent}% loss`,
		);
	}

	// Write JSON report
	await fs.mkdir(path.dirname(REPORT_PATH), { recursive: true });
	await fs.writeFile(REPORT_PATH, JSON.stringify(matrixOutput, null, 2));
	console.log(`\nReport: reports/ci/device-matrix.json`);

	// --auto: write Playwright config
	if (IS_AUTO) {
		const projectEntries = projects
			.map(
				(p) => `    {
      name: "${p.name}",
      use: {
        viewport: { width: ${p.viewport.width}, height: ${p.viewport.height} },
        userAgent: "${p.userAgent}",
        hasTouch: true,
        isMobile: true,
      },
      // ${p.description} — target ${p.target_fps}fps (${p.orientation})
    }`,
			)
			.join(",\n");

		const configContent = `// AUTO-GENERATED by scripts/ci/generate-device-matrix.mjs
// Run: bun run test:device-matrix --auto  to regenerate
// Generated: ${new Date().toISOString()}
// ${matrixOutput.testCombinations} total combinations (${projects.length} devices × ${networkLanes.length} network lanes)

import { defineConfig } from "@playwright/test";

export default defineConfig({
  testDir: "./tests/e2e",
  projects: [
${projectEntries}
  ],
});
`;

		await fs.writeFile(CONFIG_OUTPUT, configContent);
		console.log(`Playwright config written: playwright.device-matrix.config.ts`);
	} else {
		console.log(
			`\nTip: Run with --auto to write playwright.device-matrix.config.ts`,
		);
	}
};

main().catch((err) => {
	console.error("[generate-device-matrix] Failed:", err?.message || err);
	process.exit(1);
});
